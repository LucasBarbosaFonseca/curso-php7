<?php

    //Operador de Incremento ++
    /* Esse operador serve para incrementarmos (somar) uma quuantidade desejada a um determinado valor de
    uma variável */
    $a = 10;

    echo $a++; 
    /* Podemos usar o operador de incremento desse modo $a++ que incrementa depois e desse modo ++$a que 
    incrementa antes */
    echo "<br>";

    echo $a;

    echo "<br>";

    //Para usar o incremento dessa forma ++$a:
    echo ++$a;


    echo "<br>";


    //Operador de Decremento --
    /* Esse operador serve para decrementarmos (subtrair) uma quuantidade desejada de um determinado valor de
    uma variável */
    $b = 20;

    echo $b--;
    /* Podemos usar o operador de decremento desse modo $b-- que incrementa depois e desse modo --$b que 
    decrementa antes */
    echo "<br>";

    echo $b;

    echo "<br>";

    //Para usar o incremento dessa forma --$b:
    echo --$b;

?>