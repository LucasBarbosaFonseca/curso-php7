<?php

    //Operadores Aritméticos 
    
    //Os operadores aritméticos nos possibilitam fazer operações matemática em nossas aplicações. Alguns deles são:

    //Operador Adição +
    $a = 10;
    $b = 4;
    echo $a + $b;

    echo "<br>";
    
    //Operador de Subtração -
    echo $a - $b;

    echo "<br>";

    //Operador de Multiplicação *
    echo $a * $b;

    echo "<br>";

    //Operador de Divisão /
    echo $a / $b;

    echo "<br>";

    //Operador Módulo % (resto da divisão) 
    echo $a % $b;

    echo "<br>";

    //Operador Exponenciação ** (número elevado a algum outro número)
    echo $a ** $b;



?>